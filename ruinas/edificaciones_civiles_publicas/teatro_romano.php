                <p>La Civitate de Auca Patricia, en Cerezo de Río Tirón, albergó al menos dos teatros romanos, testimoniando su importancia cultural y social en la antigüedad.</p>

                <h2>Teatro de Los Palacios (Siglo I)</h2>
                <p>Uno de los teatros, y el más antiguo, se encontraba en la zona de Los Palacios. Datado en el siglo I, de esta estructura aún se pueden apreciar restos del hormigón romano característico, el <em>opus caementicium</em>. Aunque gran parte de su estructura original se ha perdido con el tiempo, los vestigios permiten comprender su temprana existencia en la ciudad.</p>

                <h2>Teatro de Las Canteras (Siglo III o IV)</h2>
                <p>Otro teatro, posterior, datado entre los siglos III o IV, se ubicaba en el término de La Arena, en la zona de Las Canteras (Canteras Este de piedra de arena). Según relatos locales, la veta de piedra de esta cantera se agotó, lo que podría estar relacionado con la construcción y posterior expolio del teatro. El puente conocido como "Puente del Gordo" es un punto de referencia para acceder a la zona donde se encontraba este teatro, cercano al puente romano del teatro de Las Canteras (posiblemente el puente de San Ciprián).</p>
                <p>La decadencia de este teatro, al igual que ocurrió con el teatro romano de Caesaraugusta (Zaragoza), comenzó en el siglo III. En este periodo, sus piedras fueron reutilizadas para la construcción y refuerzo de las murallas de la ciudad y otras edificaciones. Este expolio continuado a lo largo del tiempo es la razón por la que hoy día principalmente se conserva el <em>opus caementicium</em> de su estructura.</p>
                <p>Existe un dicho popular que reza: "No hay alcazaba o alcázar Árabe si no hay un teatro romano al que robarle las piedras". Esta máxima, que alude a la práctica común de reutilizar materiales de construcciones anteriores, podría aplicarse a Auca Patricia. La zona de Las Canteras, con su forma y medidas compatibles con las de un teatro, y siendo la única veta de piedra de arena en Cerezo, sugiere fuertemente la ubicación de este edificio. Se especula que la Fuente de los Campos, conocida por la calidad de su agua, podría haber tenido un origen romano, quizás un conducto que abastecía al teatro.</p>

                <p>La existencia de estos teatros subraya la relevancia de Auca Patricia como un centro urbano destacado en la Hispania Romana.</p>
